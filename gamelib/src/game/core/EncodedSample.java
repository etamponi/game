package game.core;

public class EncodedSample {

	private double[] input;
	private double[] output;
	
	public EncodedSample(double[] input, double[] output) {
		this.input = input;
		this.output = output;
	}
	
	public double[] getInput() {
		return input;
	}
	
	public double[] getOutput() {
		return output;
	}
	
}
